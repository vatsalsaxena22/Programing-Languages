# Data Types

a = "Hello World" # str
b = 20 # int
c = 55.5 # float
d = range(6) # range
e = True # bool
f = ["apple", "banana", "cherry"] # list
g = ("apple", "banana", "cherry") # tuple
h = {"name" : "John", "age" : 36} # dict
i = {"apple", "banana", "cherry"} # set
j = None # None

print(a)
print(b)
print(c)
print(d)
print(e)
print(f)
print(g)
print(h)
print(i)
print(j)

# Type casting

x = str(b) # convert int to str
y = int(c) # convert float to int
z = bool(1) # convert int to bool

print(type(x), type(y), z)